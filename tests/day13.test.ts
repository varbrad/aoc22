import { describe, expect, it } from 'vitest'

import * as day13 from '../days/day13'

describe('D13P1', () => {
  it('should solve example case', () => {
    const input = `[1,1,3,1,1]
[1,1,5,1,1]

[[1],[2,3,4]]
[[1],4]

[9]
[[8,7,6]]

[[4,4],4,4]
[[4,4],4,4,4]

[7,7,7,7]
[7,7,7]

[]
[3]

[[[]]]
[[]]

[1,[2,[3,[4,[5,6,7]]]],8,9]
[1,[2,[3,[4,[5,6,0]]]],8,9]`
    expect(day13.part1(input)).toEqual(13)
  })
})

